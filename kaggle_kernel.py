def log_step(step_name: str, status: str, details: dict | None = None):
    """Append one line to session_log.jsonl (M3 invariant)."""
    import time as _time
    entry = {
        "timestamp": _time.strftime("%Y-%m-%dT%H:%M:%S"),
        "step": step_name,
        "status": status,
        "details": details or {},
    }
    line = json.dumps(entry)
    try:
        with open(SESSION_LOG, "a") as f:
            f.write(line + "\n")
    except (OSError, IOError):
        pass


def run_cmd(cmd, timeout=300, description="", wd=None, extra_env=None, realtime_output=True):
    """Run a shell command with timeout and output capture."""
    if description:
        print(f"  {description}")
    cwd = wd if wd else WORKING_DIR
    print(f"[cwd={cwd}] $ {' '.join(cmd)}")
    env = os.environ.copy()
    if extra_env:
        env.update(extra_env)
    
    # Generate a unique log file for this command
    import hashlib
    cmd_hash = hashlib.md5(' '.join(cmd).encode()).hexdigest()[:8]
    cmd_log_file = os.path.join(WORKING_DIR, f".cmd_{cmd_hash}.log")
    full_output = []
    full_stderr = []
    
    try:
        if realtime_output:
            # Run with real-time output
            process = subprocess.Popen(
                cmd, 
                stdout=subprocess.PIPE, 
                stderr=subprocess.PIPE, 
                text=True,
                cwd=cwd, 
                env=env,
            )
            
            # Read output in real-time with timeout
            import time
            stdout_lines = []
            stderr_lines = []
            
            print("\n=== Command Output ===")
            start_time = time.time()
            while True:
                # Check for timeout
                if time.time() - start_time > timeout:
                    process.kill()
                    raise subprocess.TimeoutExpired(cmd, timeout)
                
                # Read output with non-blocking
                import select
                ready, _, _ = select.select([process.stdout, process.stderr], [], [], 1.0)
                
                if process.stdout in ready:
                    stdout_line = process.stdout.readline()
                    if stdout_line:
                        print(stdout_line, end="")
                        stdout_lines.append(stdout_line)
                
                if process.stderr in ready:
                    stderr_line = process.stderr.readline()
                    if stderr_line:
                        print(f"STDERR: {stderr_line}", end="")
                        stderr_lines.append(stderr_line)
                
                # Check if process has exited
                if process.poll() is not None:
                    # Read any remaining output
                    while True:
                        stdout_line = process.stdout.readline()
                        if not stdout_line:
                            break
                        print(stdout_line, end="")
                        stdout_lines.append(stdout_line)
                    
                    while True:
                        stderr_line = process.stderr.readline()
                        if not stderr_line:
                            break
                        print(f"STDERR: {stderr_line}", end="")
                        stderr_lines.append(stderr_line)
                    break
            
            returncode = process.wait()
            stdout = ''.join(stdout_lines)
            stderr = ''.join(stderr_lines)
        else:
            # Run with captured output
            r = subprocess.run(
                cmd, capture_output=True, text=True,
                timeout=timeout, cwd=cwd, env=env,
            )
            returncode = r.returncode
            stdout = r.stdout
            stderr = r.stderr
        
        # Save full output to log file
        try:
            with open(cmd_log_file, "w") as log_f:
                log_f.write(f"Command: {' '.join(cmd)}\n")
                log_f.write(f"CWD: {cwd}\n")
                log_f.write(f"Exit code: {returncode}\n\n")
                
                log_f.write("STDOUT:\n")
                log_f.write(stdout)
                log_f.write("\n\nSTDERR:\n")
                log_f.write(stderr)
        except (OSError, IOError) as e:
            print(f"Failed to write command log: {e}")
        
        if not realtime_output:
            if stdout:
                out = stdout
                if len(out) > 5000:
                    # Save full output to a file so nothing is lost
                    full_out_path = os.path.join(WORKING_DIR, f".cmd_output_{cmd_hash}.log")
                    try:
                        with open(full_out_path, "w") as _f:
                            _f.write(out)
                    except (OSError, IOError):
                        pass
                    print(f"... ({len(out)} chars total, showing first 3000 + last 2000)")
                    print(out[:3000])
                    print("\n... [truncated] ...\n")
                    print(out[-2000:])
                    print(f"Full output saved to: .cmd_output_{cmd_hash}.log")
                else:
                    print(out)
            if stderr:
                print(f"STDERR: {stderr[-1000:]}")
                if len(stderr) > 1000:
                    print(f"Full stderr saved to: .cmd_{cmd_hash}.log")
        
        print(f"\nExit code: {returncode}")
        print(f"Command log: .cmd_{cmd_hash}.log")
        return returncode == 0, stdout, stderr
    except subprocess.TimeoutExpired:
        print(f"\nTimeout after {timeout}s")
        # Save timeout information to log file
        try:
            with open(cmd_log_file, "w") as log_f:
                log_f.write(f"Command: {' '.join(cmd)}\n")
                log_f.write(f"CWD: {cwd}\n")
                log_f.write(f"Error: Timeout after {timeout}s\n")
        except (OSError, IOError):
            pass
        return False, "", "Timeout"
    except Exception as e:
        print(f"\nError: {e}")
        # Save exception information to log file
        try:
            with open(cmd_log_file, "w") as log_f:
                log_f.write(f"Command: {' '.join(cmd)}\n")
                log_f.write(f"CWD: {cwd}\n")
                log_f.write(f"Error: {str(e)}\n")
                import traceback
                log_f.write(traceback.format_exc())
        except (OSError, IOError):
            pass
        return False, "", str(e)


# ── Secret Management ────────────────────────────────────────────────

def get_kaggle_secret(secret_name: str) -> str | None:
    """Read a secret from Kaggle's Secrets storage (4 methods)."""
    # Method 1: kaggle_secrets module (available in Kaggle kernels)
    masked_name = secret_name[:3] + "***" if len(secret_name) > 3 else "***"
    try:
        from kaggle_secrets import UserSecretsClient
        client = UserSecretsClient()
        val = client.get_secret(secret_name)
        if val:
            print(f"[secret] {masked_name}: found via kaggle_secrets")
            return val
    except Exception as e:
        print(f"[secret] {masked_name}: kaggle_secrets failed: {e}")

    # Method 2: /kaggle/secrets/ file mount
    secret_path = f"/kaggle/secrets/{secret_name}"
    if os.path.isfile(secret_path):
        with open(secret_path) as f:
            val = f.read().strip()
        if val:
            print(f"[secret] {masked_name}: found via file mount")
            return val

    # Method 3: Direct environment variable
    val = os.environ.get(secret_name, "")
    if val:
        print(f"[secret] {masked_name}: found via env var")
        return val

    # Method 4: /kaggle/input/ mount
    secret_input_path = f"/kaggle/input/{secret_name}"
    if os.path.isfile(secret_input_path):
        with open(secret_input_path) as f:
            val = f.read().strip()
        if val:
            print(f"[secret] {masked_name}: found via /kaggle/input")
            return val

    masked_name = secret_name[:3] + "***" if len(secret_name) > 3 else "***"
    print(f"[secret] {masked_name}: not found")
    return None


# ── Step 1: Environment Check ────────────────────────────────────────

def check_environment():
    """Verify GPU, nvcc, and optionally ncu are available."""
    banner("1. Environment Check")
    ok, out, err = run_cmd(["nvidia-smi", "-L"], description="GPU check")
    if not ok:
        print("No GPU detected -- aborting")
        return False

    # Print GPU details
    print("\nFull nvidia-smi output:")
    run_cmd(["nvidia-smi"], description="GPU details")

    ok, out, err = run_cmd(["nvcc", "--version"], description="nvcc check")
    if not ok:
        print("nvcc not found -- Kaggle GPU image should include CUDA, aborting")
        return False

    ok, _, _ = run_cmd(["which", "ncu"], description="ncu check")
    if ok:
        print("ncu available -- will use Nsight Compute profiling")
    else:
        print("ncu not found -- will use cudaEventElapsedTime fallback")

    return True


