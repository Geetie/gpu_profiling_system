    if not provider_configs:
        print("No LLM API keys found in Kaggle Secrets -- probes-only mode")
        return False

    # Set environment variables (so ProviderManager can auto-detect)
    for k, v in provider_configs["env"].items():
        os.environ[k] = v

    # Write config file for the pipeline
    cfg = {
        "env": provider_configs["env"],
        "includeCoAuthoredBy": False,
        "effortLevel": "high",
    }
    os.makedirs(os.path.dirname(config_path), exist_ok=True)
