# ── Step 6: Results Analysis ─────────────────────────────────────────

def analyze_results() -> bool:
    """Read and display results.json."""
    banner("5. Results Analysis")
    results_path = os.path.join(WORKING_DIR, "results.json")

    if not os.path.isfile(results_path):
        print(f"ERROR: results.json not found at {results_path}")
        print("Directory listing:")
        for f in sorted(os.listdir(WORKING_DIR)):
            print(f"  {f}")
        return False

    with open(results_path) as f:
        results = json.load(f)

    # Extract measurements (may be at top level or nested)
    measurements = results.get("measurements", {})
    if not measurements:
        # Top-level numeric keys
        measurements = {
            k: v for k, v in results.items() if isinstance(v, (int, float))
        }

    print()
    print("Measurements:")
    key_metrics = [
        "actual_boost_clock_mhz",
        "dram_latency_cycles",
        "l2_latency_cycles",
        "l1_latency_cycles",
        "l2_cache_size_mb",
        "dram_bandwidth_gbps",
        "shmem_bandwidth_gbps",
        "max_shmem_per_block_kb",
        "bank_conflict_penalty_ratio",
        "sm_count",
        "likely_gpu_family",
    ]
    for metric in key_metrics:
        if metric in measurements:
            print(f"  {metric}: {measurements[metric]}")

    # Cross-validation
    cv = results.get("cross_validation", {})
    if cv:
        passed = sum(1 for v in cv.values() if v is True)
        print(f"\nCross-validation: {passed}/{len(cv)} passed")
        failed = [k for k, v in cv.items() if v is False]
        if failed:
            print(f"  Failed checks: {failed}")

    # Probe status
    probe_status = results.get("probe_status", {})
    if probe_status:
        print(f"\nProbe status:")
        for name, status in probe_status.items():
            mark = "OK" if status == "success" else "!!"
            print(f"  [{mark}] {name}: {status}")

    # Methodology
    methodology = results.get("methodology", "")
    print(f"\nMethodology length: {len(methodology)} chars")

    # Evidence files
    evidence = results.get("evidence", [])
    print(f"Evidence files: {len(evidence)}")

    return True


