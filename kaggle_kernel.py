PROJECT_ROOT = None
all_errors = []
probe_ok = False
pipeline_ok = False
api_configured = False
results_ok = False

try:
    banner("GPU Profiling System - Kaggle Test")

    # ── Step 0: Clone / locate repo ──────────────────────────────
    banner("0. Repository Setup")
    PROJECT_ROOT = os.path.join(WORKING_DIR, "gpu_profiling_system")

    if os.path.isfile(os.path.join(PROJECT_ROOT, "src", "main.py")):
        print("Project already exists -- skipping clone")
    else:
        ok, out, err = run_cmd(
            [
                "git",
                "clone",
                "https://github.com/Geetie/gpu_profiling_system.git",
                PROJECT_ROOT,
            ],
            timeout=120,
            description="Clone repository",
        )
        if not ok:
            all_errors.append("Failed to clone repository")
            print("ERROR: Failed to clone repository")
            sys.exit(1)

    os.chdir(PROJECT_ROOT)
    sys.path.insert(0, PROJECT_ROOT)
    print(f"Project root: {PROJECT_ROOT}")
    print(f"Current dir: {os.getcwd()}")
    print(f"src/main.py exists: {os.path.isfile(os.path.join(PROJECT_ROOT, 'src', 'main.py'))}")

    # ── Step 1: Environment ──────────────────────────────────────
    if not check_environment():
        all_errors.append("Environment check failed")
        print("Environment check failed -- aborting")
        sys.exit(1)

    # ── Step 2: API Config ───────────────────────────────────────
    api_configured = configure_api(PROJECT_ROOT)

    # ── Step 3: Target Spec ──────────────────────────────────────
    target_spec_path = create_target_spec(PROJECT_ROOT)

    # ── Step 4: Hardware Probes (always runs) ────────────────────
    probe_ok = run_probes(PROJECT_ROOT)
    if not probe_ok:
        all_errors.append("Hardware probes failed")

    # ── Step 5: Pipeline (only if API configured and probes OK) ──
    if api_configured and probe_ok:
        pipeline_ok = run_pipeline(PROJECT_ROOT, target_spec_path)
        if not pipeline_ok:
            all_errors.append("Pipeline failed")
    else:
        banner("Pipeline - SKIPPED")
        reason = []
        if not api_configured:
            reason.append("no API key")
        if not probe_ok:
            reason.append("probes failed")
        msg = f"Pipeline skipped: {', '.join(reason)}"
        all_errors.append(msg)
        print(msg)

    # ── Step 6: Results ──────────────────────────────────────────
    results_ok = analyze_results()
    if not results_ok:
        all_errors.append("Results analysis failed")

    # ── Step 7: Collect artifacts ────────────────────────────────
    copy_artifacts(PROJECT_ROOT)

except Exception as e:
    all_errors.append(f"Fatal error: {str(e)}")
    print(f"\nFATAL ERROR: {e}")
    traceback.print_exc()

finally:
    # ── Execution Summary ────────────────────────────────────────
    banner("Execution Summary")
    rp = os.path.join(WORKING_DIR, "results.json")
    ps = "PASS" if probe_ok else "FAIL"
    pl = "PASS" if pipeline_ok else "SKIP"
    rs = "FOUND" if os.path.isfile(rp) else "MISSING"

    print(f"  Hardware probes: {ps}")
    print(f"  Pipeline mode:   {pl}")
    print(f"  results.json:    {rs}")
    print(f"  API configured:  {api_configured}")
    if os.path.isfile(rp):
        print(f"  results.json size: {os.path.getsize(rp):,} bytes")

    # Write summary file
    summary = {
        "probe_ok": probe_ok,
        "pipeline_ok": pipeline_ok,
        "api_configured": api_configured,
        "results_ok": results_ok,
        "results_found": os.path.isfile(rp),
        "errors": all_errors,
        "project_root": PROJECT_ROOT,
        "working_dir": WORKING_DIR,
    }
    summary_path = os.path.join(WORKING_DIR, "execution_summary.json")
    with open(summary_path, "w") as f:
        json.dump(summary, f, indent=2)
    print(f"\nExecution summary written to: {summary_path}")

    if all_errors:
        print(f"\nERRORS ({len(all_errors)}):")
        for i, err in enumerate(all_errors, 1):
            print(f"  {i}. {err}")

    print("\nTest complete.")
    print(f"Log file: {LOG_FILE}")
    log_fh.close()
