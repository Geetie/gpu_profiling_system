"""GPU Profiling System - Kaggle Kernel (Real GPU Environment).

Runs the complete multi-agent GPU profiling pipeline on Kaggle's T4/P100 GPUs.
No hardcoded CUDA code — everything is generated by agents or hardware probes.

Pipeline flow:
  Environment Check → API Config → Target Spec → Hardware Probes → Agent Pipeline → Results

Usage: Run as a Kaggle notebook/kernel with GPU enabled.
Required Kaggle Secrets: LONGCAT_API_KEY, DASHSCOPE_API_KEY, or ANTHROPIC_API_KEY
"""
from __future__ import annotations

