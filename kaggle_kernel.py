class TeeWriter:
    def __init__(self, stream, file):
        self._stream = stream
        self._file = file

    def write(self, s):
        self._stream.write(s)
        if self._file:
            self._file.write(s)
            self._file.flush()

    def flush(self):
        self._stream.flush()
        if self._file:
            self._file.flush()


sys.stdout = TeeWriter(sys.stdout, log_fh)
