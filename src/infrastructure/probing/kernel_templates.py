"""CUDA kernel design specifications for hardware probing.

This module provides DESIGN SPECIFICATIONS (not hardcoded templates) that guide
LLM-based code generation. Each specification describes:

1. The measurement objective
2. Required techniques and algorithms
3. Timing methodology
4. Output format requirements
5. Architecture-agnostic best practices

Per spec.md: "CUDA/C++ code should be generated by LLM, not hardcoded templates."
This module contains NO hardcoded CUDA source code — only design principles.
"""
from __future__ import annotations

from dataclasses import dataclass


@dataclass
class KernelDesignSpec:
    """Design specification for a CUDA micro-benchmark kernel.
    
    Contains design principles that an LLM uses to generate actual CUDA code.
    NO hardcoded source code — only methodology descriptions.
    """
    name: str
    objective: str
    timing_method: str
    key_techniques: list[str]
    output_format: list[str]
    working_set_guidance: str
    architecture_notes: str


def get_pointer_chase_design_spec() -> KernelDesignSpec:
    """Design specification for pointer-chasing latency measurement.
    
    Used for measuring memory hierarchy latency (L1, L2, DRAM).
    The LLM generates CUDA code following these design principles.
    """
    return KernelDesignSpec(
        name="pointer_chase",
        objective=(
            "Measure memory access latency in SM clock cycles by following "
            "a linked list through GPU memory. Different working set sizes "
            "target different cache levels: L1 (< 32 KB), L2 (< L2 capacity), DRAM (> L2)."
        ),
        timing_method=(
            "Use clock64() for cycle-accurate device-side timing. "
            "NEVER use clock() — returns 0 on Pascal+ under PTX JIT. "
            "Single-thread measurement (1 block, 1 thread)."
        ),
        key_techniques=[
            "Build random permutation (Knuth shuffle) on host to defeat hardware prefetcher",
            "Make chain circular: last element points back to first",
            "Warmup: traverse chain 100 times before timing to ensure pages are resident",
            "Use __threadfence() before timing to ensure warmup completes",
            "Write result to device memory to prevent dead-code elimination",
            "Calculate cycles_per_access = total_cycles / iterations",
        ],
        output_format=[
            "total_cycles: <integer>",
            "iterations: <integer>",
            "cycles_per_access: <float>",
            "chain_checksum: <integer>  # prevents dead-code elimination",
        ],
        working_set_guidance=(
            "L1 latency: array_size = 2048 (8 KB) — fits in all GPU L1 caches\n"
            "L2 latency: array_size = 524288 (2 MB) — fits in most L2 caches\n"
            "DRAM latency: array_size = 16777216 (64 MB) — exceeds all known L2 sizes"
        ),
        architecture_notes=(
            "clock64() works on all CUDA architectures (sm_35+)\n"
            "Random permutation defeats hardware prefetcher on all architectures\n"
            "No architecture-specific intrinsics required"
        ),
    )


def get_working_set_sweep_design_spec() -> KernelDesignSpec:
    """Design specification for cache capacity detection via working-set sweep."""
    return KernelDesignSpec(
        name="working_set_sweep",
        objective=(
            "Detect L2 cache capacity by sweeping working set sizes and "
            "identifying the 'cliff' where latency jumps sharply (cache overflow)."
        ),
        timing_method=(
            "Use clock64() for device-side timing. "
            "Measure cycles per access at each working set size. "
            "The cliff is where cycles_per_access increases by > 3x."
        ),
        key_techniques=[
            "Use random permutation (Knuth shuffle) — defeats hardware prefetcher",
            "Sweep sizes from 1 MB to 128 MB with intermediate points",
            "Common L2 capacities: 2-6 MB (consumer), 40-72 MB (datacenter)",
            "Report the last size BEFORE the cliff as cache capacity",
        ],
        output_format=[
            "size_bytes: <integer>",
            "total_cycles: <integer>",
            "cycles_per_access: <float>",
            "iterations: <integer>",
        ],
        working_set_guidance=(
            "Suggested sweep points (in elements, each = 4 bytes):\n"
            "256K, 512K, 1M, 2M, 3M, 4M, 5M, 6M, 8M, 10M, 12M, 16M, 24M, 32M\n"
            "This covers 1 MB to 128 MB working sets"
        ),
        architecture_notes=(
            "L2 capacity varies by GPU:\n"
            "- Pascal (P100): 4 MB\n"
            "- Volta (V100): 6 MB\n"
            "- Turing (T4): 6 MB\n"
            "- Ampere (A100): 40-80 MB\n"
            "- Hopper (H100): 50-60 MB\n"
            "Measurement should detect actual capacity, not assume these values"
        ),
    )


def get_clock_calibration_design_spec() -> KernelDesignSpec:
    """Design specification for SM clock frequency measurement."""
    return KernelDesignSpec(
        name="clock_calibration",
        objective=(
            "Measure actual SM clock frequency by combining device-side "
            "cycle counts with wall-clock timing. This defeats frequency "
            "locking because it measures actual frequency, not spec sheet values."
        ),
        timing_method=(
            "Device-side: clock64() for total SM cycles\n"
            "Host-side: cudaEventElapsedTime OR ncu GPU time for wall-clock\n"
            "actual_freq_MHz = total_cycles / elapsed_time_us"
        ),
        key_techniques=[
            "Use random permutation linked list for stable timing",
            "Small working set (4 KB) to minimize memory latency variance",
            "Run multiple iterations (10M+) for measurable elapsed time",
            "Use cudaEventRecord/cudaEventSynchronize for host timing",
            "Run 3 trials, take minimum to reduce noise",
        ],
        output_format=[
            "total_cycles: <integer>",
            "iterations: <integer>",
            "cycles_per_iter: <float>",
            "elapsed_time_ms: <float>  # from cudaEventElapsedTime",
        ],
        working_set_guidance=(
            "Use small working set: 1024 elements (4 KB)\n"
            "This fits in L1 cache, minimizing memory latency variance\n"
            "Large iteration count (10M+) ensures measurable elapsed time"
        ),
        architecture_notes=(
            "clock64() works on all architectures\n"
            "cudaEventElapsedTime has ~0.5 microsecond precision\n"
            "Typical GPU clocks: 500-2500 MHz\n"
            "Frequency may be locked below spec by nvidia-smi"
        ),
    )


