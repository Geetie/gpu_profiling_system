def get_shmem_capacity_design_spec() -> KernelDesignSpec:
    """Design specification for shared memory capacity detection."""
    return KernelDesignSpec(
        name="shmem_capacity",
        objective=(
            "Determine maximum shared memory per block using CUDA occupancy API. "
            "Query cudaDeviceGetAttribute and sweep shmem sizes."
        ),
        timing_method=(
            "NO timing needed — uses cudaOccupancyMaxActiveBlocksPerMultiprocessor. "
            "This is a direct API query, not a micro-benchmark."
        ),
        key_techniques=[
            "Query cudaDevAttrMaxSharedMemoryPerBlock for device limit",
            "Query cudaDevAttrMaxSharedMemoryPerMultiprocessor for SM limit",
            "Use cudaOccupancyMaxActiveBlocksPerMultiprocessor to verify",
            "Sweep shmem sizes: 1KB, 2KB, 4KB, ..., up to device max",
            "Find largest size where blocks_per_sm > 0",
            "Use cudaFuncSetAttribute to allow large dynamic shmem",
        ],
        output_format=[
            "device_max_shared_mem_per_block: <integer>",
            "device_max_shared_mem_per_sm: <integer>",
            "compute_capability: <string>  # e.g., '60' for sm_60",
            "shmem_<size>: blocks=<n> active_warps=<n>",
            "shmem_max_tested: <integer>",
        ],
        working_set_guidance=(
            "Sweep sizes: 1KB, 2KB, 4KB, 8KB, 16KB, 32KB, 48KB, 64KB, 96KB, 128KB, ...\n"
            "Typical limits:\n"
            "- sm_60 (Pascal): 64 KB/block\n"
            "- sm_70 (Volta): 96 KB/block\n"
            "- sm_75+ (Turing/Ampere): up to 164 KB/block"
        ),
        architecture_notes=(
            "Use cudaDeviceGetAttribute, NOT cudaGetDeviceProperties\n"
            "cudaGetDeviceProperties may be virtualized under vGPU/MIG\n"
            "cudaDeviceGetAttribute returns actual hardware values"
        ),
    )


def get_bank_conflict_design_spec() -> KernelDesignSpec:
    """Design specification for shared memory bank conflict latency measurement."""
    return KernelDesignSpec(
        name="bank_conflict",
        objective=(
            "Measure the latency penalty of shared memory bank conflicts "
            "by comparing strided access (conflicts) vs sequential access (no conflicts)."
        ),
        timing_method=(
            "Use clock64() for device-side timing within the kernel. "
            "Measure both patterns in the SAME kernel execution to ensure "
            "identical conditions. Ratio = strided_cycles / sequential_cycles."
        ),
        key_techniques=[
            "Use volatile __shared__ to prevent compiler caching",
            "Strided access: thread t accesses element t * stride (stride=32)",
            "Sequential access: thread t accesses element t + offset",
            "Both patterns access same number of elements",
            "Run 3 trials, take minimum for each pattern",
        ],
        output_format=[
            "strided_cycles: <integer>",
            "sequential_cycles: <integer>",
            "bank_conflict_ratio: <float>",
            "stride: <integer>",
            "size: <integer>",
            "iterations: <integer>",
        ],
        working_set_guidance=(
            "Use 32K floats (128 KB) shared memory\n"
            "Stride = 32 causes all threads in a warp to access same bank\n"
            "256 threads = 8 warps, all hitting same bank = worst case"
        ),
        architecture_notes=(
            "All NVIDIA GPUs have 32 shared memory banks\n"
            "Bank conflict ratio typically 16-32x for worst case\n"
            "Compile with -O0 to prevent aggressive optimization\n"
            "volatile shared memory ensures actual memory access"
        ),
    )


def get_shmem_bandwidth_design_spec() -> KernelDesignSpec:
    """Design specification for shared memory bandwidth measurement."""
    return KernelDesignSpec(
        name="shmem_bandwidth",
        objective=(
            "Measure shared memory bandwidth by having all threads in a block "
            "cooperatively read and write shared memory at maximum rate."
        ),
        timing_method=(
            "Use cudaEventElapsedTime for wall-clock timing. "
            "Bandwidth = total_bytes_transferred / elapsed_seconds. "
            "Total bytes = iterations × threads × 2 × sizeof(float) (read + write)."
        ),
        key_techniques=[
            "All threads read entire shared memory, then write back",
            "Use 256 threads per block",
            "Multiple iterations (10K+) for measurable time",
            "Prevent dead-code elimination with volatile or checksum",
            "Warmup run before measurement",
        ],
        output_format=[
            "shmem_bytes: <integer>",
            "block_threads: <integer>",
            "iterations: <integer>",
            "total_bytes_transferred: <integer>",
            "gpu_time_ms: <float>",
            "shmem_bandwidth_gbps: <float>",
        ],
        working_set_guidance=(
            "Shared memory size = threads × sizeof(float) = 256 × 4 = 1 KB\n"
            "This is small enough to fit in any GPU's shared memory\n"
            "Focus is on throughput, not capacity"
        ),
        architecture_notes=(
            "Shared memory bandwidth varies by architecture:\n"
            "- Typically 500-3000 GB/s\n"
            "- Higher on newer architectures with more banks\n"
            "Compile with -O3 for maximum throughput"
        ),
    )


