def auto_detect_provider(pm):
    """自动检测供应商"""
    print("\n🔍 正在自动检测供应商...")
    provider = pm.detect_provider()

    if provider:
        print(f"✅ 检测到: {provider.name}")
        config = pm.create_unified_config()
        if config:
            save_config(config)
            print("✅ 配置文件已生成")
    else:
        print("❌ 未检测到可用供应商")
        print("\n💡 请设置以下环境变量之一:")
        print("  - DASHSCOPE_API_KEY (阿里云百炼)")
        print("  - LONGCAT_API_KEY (LongCat)")
        print("  - ANTHROPIC_API_KEY (Anthropic)")


def manual_select_provider(pm):
    """手动选择供应商"""
    print("\n📝 手动选择供应商:")

    providers = list(pm.providers.keys())
    for i, name in enumerate(providers, 1):
        provider = pm.providers[name]
        api_key = provider.get_api_key()
        status = "✅" if api_key else "❌"
        print(f"{i}. {provider.name} {status}")

    try:
        choice = int(input(f"\n选择供应商 (1-{len(providers)}): ")) - 1
        if 0 <= choice < len(providers):
            provider_name = providers[choice]
            if pm.set_provider(provider_name):
                config = pm.create_unified_config()
                if config:
                    save_config(config)
                    print("✅ 配置完成")
            else:
                print("❌ 配置失败")
        else:
            print("❌ 无效选择")
    except ValueError:
        print("❌ 请输入数字")


