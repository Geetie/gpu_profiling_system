def generate_config_file(pm):
    """生成配置文件"""
    print("\n📄 生成配置文件...")

    config = pm.create_unified_config()
    if not config:
        print("❌ 无法创建配置")
        return

    save_config(config)
    print("✅ 配置文件已保存到 config/api_config.json")


def save_config(config):
    """保存配置文件"""
    config_path = Path("config/api_config.json")
    config_path.parent.mkdir(exist_ok=True)

    # 只保存env部分，保持与原有格式兼容
    simple_config = {
        "_comment": f"Generated by provider_manager - {config['provider_name']}",
        "env": config["env"],
        "includeCoAuthoredBy": False,
        "effortLevel": "high"
    }

    with open(config_path, 'w', encoding='utf-8') as f:
        json.dump(simple_config, f, indent=2, ensure_ascii=False)


